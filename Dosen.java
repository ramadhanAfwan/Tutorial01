
public class Dosen {
	public String namaDosen;
	public int nip;
	
	public Dosen(String namaDosen, int nip) {
		super();
		this.namaDosen = namaDosen;
		this.nip = nip;
	}

	public String getNamaDosen() {
		return namaDosen;
	}

	public void setNamaDosen(String namaDosen) {
		this.namaDosen = namaDosen;
	}

	public int getNip() {
		return nip;
	}

	public void setNip(int nip) {
		this.nip = nip;
	}
	
	
}
